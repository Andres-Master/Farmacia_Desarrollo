    public MarcaProducto devuelveObjeto(int cas, ArrayList<MarcaProducto> lista) {
        MarcaProducto objeto1 = null;
        for (int i = 0; i < lista.size(); i++) {
            if (cas == lista.get(i).getId_Marcas()) {
                objeto1 = lista.get(i);
                break;
            }
        }
        return objeto1;
    }

